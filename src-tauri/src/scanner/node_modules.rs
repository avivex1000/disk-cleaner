use super::dir_size;
use super::Scanner;
use crate::types::{Category, DiskItem, ItemType};
use std::path::Path;
use walkdir::WalkDir;

pub struct NodeModulesScanner;

const MAX_DEPTH: usize = 6;
const MIN_SIZE: u64 = 10_000_000; // 10 MB

/// Directories inside $HOME that should never be searched for node_modules.
const SKIP_HOME_CHILDREN: &[&str] = &[
    "Library", "Applications", "Music", "Movies", "Pictures", "Photos",
    ".Trash", ".cache", ".local", ".cargo", ".rustup", ".npm", ".bun",
    ".docker", ".orbstack", ".gradle", ".cocoapods", ".pyenv", ".conda",
];

impl Scanner for NodeModulesScanner {
    fn id(&self) -> &str { "node_modules" }
    fn name(&self) -> &str { "node_modules" }
    fn icon(&self) -> &str { "📦" }

    fn scan(&self, home: &Path) -> Option<Category> {
        let mut items = Vec::new();

        // Dynamically discover which top-level home directories to search.
        // We scan every visible directory in $HOME except known non-project dirs.
        let search_roots = discover_search_roots(home);

        for search_root in &search_roots {
            let walker = WalkDir::new(search_root)
                .max_depth(MAX_DEPTH)
                .into_iter()
                .filter_entry(|e| {
                    let name = e.file_name().to_string_lossy();
                    // Skip hidden dirs and don't recurse into node_modules
                    if e.depth() > 0 && name.starts_with('.') {
                        return false;
                    }
                    if name == "node_modules" {
                        return false;
                    }
                    true
                });

            for entry in walker.flatten() {
                if entry.file_type().is_dir() {
                    let nm_path = entry.path().join("node_modules");
                    if nm_path.is_dir() {
                        let size = dir_size(&nm_path);
                        if size >= MIN_SIZE {
                            let project = entry.file_name().to_string_lossy().to_string();
                            items.push(DiskItem {
                                path: nm_path.to_string_lossy().to_string(),
                                size_bytes: size,
                                item_type: ItemType::Directory,
                                description: format!("node_modules in {project}"),
                    orphaned: false,
                            });
                        }
                    }
                }
            }
        }

        items.sort_by(|a, b| b.size_bytes.cmp(&a.size_bytes));

        let total: u64 = items.iter().map(|i| i.size_bytes).sum();
        if items.is_empty() { return None; }

        Some(Category {
            id: self.id().into(),
            name: self.name().into(),
            icon: self.icon().into(),
            total_bytes: total,
            items,
        })
    }
}

/// Find all visible directories in $HOME that could contain projects.
fn discover_search_roots(home: &Path) -> Vec<std::path::PathBuf> {
    let mut roots = Vec::new();

    let Ok(entries) = std::fs::read_dir(home) else {
        return roots;
    };

    for entry in entries.flatten() {
        let name = entry.file_name().to_string_lossy().to_string();

        // Skip hidden directories
        if name.starts_with('.') {
            continue;
        }

        // Skip known non-project directories
        if SKIP_HOME_CHILDREN.iter().any(|s| s.eq_ignore_ascii_case(&name)) {
            continue;
        }

        let path = entry.path();
        if path.is_dir() {
            roots.push(path);
        }
    }

    roots
}
